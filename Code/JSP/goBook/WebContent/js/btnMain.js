/*退出按钮效果*/
function exitBtnOver(exit) {
	exit.style.backgroundColor="#474747";
	exit.style.color="#fff";
	exit.style.textShadow="#fff 0 0 7px";
}

function exitBtnOut(exit) {
	exit.style.backgroundColor="RGBA(71,71,71,0.5)";
	exit.style.color="#fff";
	exit.style.textShadow="none";
}

